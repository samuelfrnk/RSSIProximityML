import pandas as pd
import matplotlib.pyplot as plt

df = pd.read_csv('Experiment_3.csv')

average_rssi_per_distance = df.groupby('Distance')['RSSI-Value'].mean().reset_index()

plt.figure(figsize=(10, 6))
plt.scatter(average_rssi_per_distance['Distance'], average_rssi_per_distance['RSSI-Value'])
plt.xlabel('Distance')
plt.ylabel('Average RSSI Value')
plt.title('Average RSSI Value for Each Distance')
plt.grid(True)
plt.show()